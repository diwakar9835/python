num1=int(input("Enter first number"))

num2=int(input("Enter second number"))

op=input("Enter mathematical operator")

if op=='+':
    print("Result is ", num1+num2)
if op=='':
    print("Result is ", num1-num2)
if op=="*":
    print("Result is ", num1*num2)
if op=='/':
    print("Result is ", num1/num2)
if op=='%':
    print("Result is ", num1% num2)
if op=="**":
    print("Result is ", num1**num2)
if op=='//':
    print("Result is", num1//num2)
